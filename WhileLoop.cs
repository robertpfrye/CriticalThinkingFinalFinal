using System;
using System.Collections.Generic;
using System.Text;

namespace CriticalThinkingWorksheet
{
    class WhileLoop
    {

        //member variables
        public int whileLoop;
        public int counter = 5;
        public string whileInput;


        //constructor
        public WhileLoop()
        {
        }


        //member methods
        public void While()
        {
            while (counter != 42)
            {
                Console.WriteLine("Please enter the numerically typed number 42");
                whileInput = Console.ReadLine();
                {
                    counter = Int32.Parse(whileInput);
                }
            }
            Console.WriteLine(counter + " Thanks you");
        }


    }
}
